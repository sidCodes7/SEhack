// ─── POST /api/email/dispatch — Send an approved alert email ───────────
router.post('/dispatch', async (req, res, next) => {
  try {
    const { recipient, subject, severity, zone_name, label, confidence, reasoning } = req.body;

    if (!recipient || !subject) {
      return res.status(400).json({ error: 'recipient and subject are required' });
    }

    const html = buildHtmlBody(subject, severity, zone_name, label, confidence, reasoning);
    const transporter = await getTransporter();

    const fromAddress = process.env.GMAIL_USER || 'aquasentinel@noreply.com';

    const info = await transporter.sendMail({
      from: `"AquaSentinel Alerts" <${fromAddress}>`,
      to: recipient,
      subject: subject,
      html: html,
    });

    // If using Ethereal, get the preview URL
    const previewUrl = nodemailer.getTestMessageUrl(info);

    console.log(`[Email] Sent to ${recipient} — Message ID: ${info.messageId}`);
    if (previewUrl) console.log(`[Email] Preview URL: ${previewUrl}`);

    res.json({
      sent: true,
      messageId: info.messageId,
      previewUrl: previewUrl || null,
    });
  } catch (err) {
    console.error('[Email] Send failed:', err.message);
    res.status(500).json({ error: 'Email send failed', detail: err.message });
  }
});

// ─── GET /api/email/test — Quick test to verify SMTP ─────────────────
router.get('/test', async (_req, res, next) => {
  try {
    const transporter = await getTransporter();
    await transporter.verify();
    res.json({ smtp_ok: true, message: 'SMTP connection verified' });
  } catch (err) {
    res.status(500).json({ smtp_ok: false, error: err.message });
  }
});

export default router;
