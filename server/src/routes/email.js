import { Router } from 'express';
import nodemailer from 'nodemailer';

const router = Router();

// ── Create reusable transporter ──
// Uses Gmail SMTP if credentials are set, otherwise falls back to Ethereal test
let transporterPromise;

async function getTransporter() {
  if (transporterPromise) return transporterPromise;

  transporterPromise = (async () => {
    // Check for Gmail credentials
    if (process.env.GMAIL_USER && process.env.GMAIL_APP_PASSWORD) {
      console.log('[Email] Using Gmail SMTP transport');
      return nodemailer.createTransport({
        service: 'gmail',
        auth: {
          user: process.env.GMAIL_USER,
          pass: process.env.GMAIL_APP_PASSWORD,
        },
      });
    }

    // Fallback: use Ethereal test account
    console.log('[Email] No Gmail credentials found. Creating Ethereal test account...');
    const testAccount = await nodemailer.createTestAccount();
    console.log(`[Email] Ethereal test account: ${testAccount.user}`);
    return nodemailer.createTransport({
      host: 'smtp.ethereal.email',
      port: 587,
      secure: false,
      auth: {
        user: testAccount.user,
        pass: testAccount.pass,
      },
    });
  })();

  return transporterPromise;
}

